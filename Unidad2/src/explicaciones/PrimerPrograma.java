package explicaciones;

public class PrimerPrograma {
    public static void main(String[] args) {

        int numeroEntero;   // Una variable para guardar un número entero.
        float decimal;      // Una variable para guardar un número con parte decimal.
        String texto;       // Una variable para guardar texto.
        boolean bandera;    // Una variable para guardar un booleano.
        char caracter;      // Una variable para guardar un caracter.

        numeroEntero = 15;  // Asignamos un número a la variable entera.
        decimal = 1.2f;     // Asignamos un número a la variable decimal.
        texto = "hola!";    // Asignamos un texto a la variable string.
        bandera = true;     // Asignamos un boolean a la variable boolean.
        caracter = 'A';     // Asignamos un caracter a la variable char.

        System.out.println(numeroEntero);
        System.out.println(decimal);
        System.out.println(texto);
        System.out.println(bandera);
        System.out.println(caracter);

    }
}
